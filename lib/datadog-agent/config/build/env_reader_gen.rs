//! Build-time codegen for the typed environment reader table.
//!
//! Emits [`EnvKey`] entries for every `support: full` / `support: partial` key: the key's real
//! environment variable names, its nested path, and how to decode the raw string into the JSON
//! shape the schema declares. `env_reader` consumes this at runtime to build the typed base by
//! reading each variable directly, mirroring the Agent's per-key lookup.
//!
//! Unlike the flat-key overlay table, this emits an entry for every key, including single-segment
//! keys such as `api_key`, because the reader places values directly at their destination.
//!
//! Environment names follow the Agent's `bindEnv`:
//! - [`EnvBinding::Overridden`] declares explicit names, used verbatim in declared (priority) order.
//! - [`EnvBinding::Standard`] and [`EnvBinding::None`] derive `DD_` + `UPPER(dotted_path)` with `.`
//!   replaced by `_`. `None` (`no-env`) still derives a name because a `no-env` field can be reached
//!   through the standard form via the Agent's bespoke handling (for example `proxy.http` through
//!   `DD_PROXY_HTTP`); a field with genuinely no variable only yields a dead entry that never fires.
//!
//! The decode strategy comes from the schema's `env_parser` when present, otherwise from the field's
//! declared type.

use std::fmt::Write as _;
use std::path::Path;

use datadog_agent_config_overlay_model::schema_gen::{EnvBinding, EnvParser, FieldInfo, FieldType};
use datadog_agent_config_overlay_model::{KnownEntry, SchemaOverlay};
use indexmap::IndexMap;

pub fn generate(overlay: &SchemaOverlay, schema_map: &IndexMap<String, FieldInfo>, manifest_dir: &Path) {
    let mut keys: Vec<&String> = overlay
        .inventory
        .iter()
        .filter(|(_, entry)| matches!(entry, KnownEntry::Full(_) | KnownEntry::Partial(_)))
        .map(|(key, _)| key)
        .collect();
    keys.sort();

    let mut out = String::new();
    writeln!(
        out,
        "// @generated by build.rs from core_schema.yaml + schema_overlay.yaml — DO NOT EDIT"
    )
    .unwrap();
    writeln!(out, "// Regenerate by running `cargo build -p datadog-agent-config`.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use crate::env_decode::EnvDecode;").unwrap();
    writeln!(out, "use crate::env_reader::EnvKey;").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "/// Every modeled Datadog key: its environment variable names (highest priority first),"
    )
    .unwrap();
    writeln!(out, "/// its nested path, and how to decode the raw value.").unwrap();
    writeln!(out, "pub static DATADOG_ENV_KEYS: &[EnvKey] = &[").unwrap();

    for key in keys {
        let info = schema_map.get(key.as_str());

        let env_vars: Vec<String> = match info.map(|i| &i.env) {
            Some(EnvBinding::Overridden(env_vars)) => env_vars.clone(),
            None | Some(EnvBinding::Standard | EnvBinding::None) => vec![standard_env_name(key)],
        };
        let decode = decode_token(key, info);

        let env_vars_lit = env_vars
            .iter()
            .map(|name| format!("\"{}\"", name))
            .collect::<Vec<_>>()
            .join(", ");
        let path_lit = key
            .split('.')
            .map(|seg| format!("\"{}\"", seg))
            .collect::<Vec<_>>()
            .join(", ");

        writeln!(out, "    EnvKey {{").unwrap();
        writeln!(out, "        env_vars: &[{}],", env_vars_lit).unwrap();
        writeln!(out, "        path: &[{}],", path_lit).unwrap();
        writeln!(out, "        decode: {},", decode).unwrap();
        writeln!(out, "    }},").unwrap();
    }

    writeln!(out, "];").unwrap();

    let path = manifest_dir.join("src/generated/env_keys.rs");
    let existing = std::fs::read_to_string(&path).unwrap_or_default();
    if existing != out {
        std::fs::write(&path, &out).unwrap_or_else(|e| panic!("cannot write {}: {}", path.display(), e));
    }

    let _ = std::process::Command::new("rustfmt")
        .arg("--edition")
        .arg("2021")
        .arg(&path)
        .status();
}

/// Derives the Agent's standard environment name for a key: `DD_` + `UPPER(dotted_path)`.
fn standard_env_name(key: &str) -> String {
    format!("DD_{}", key.replace('.', "_").to_uppercase())
}

/// The `EnvDecode` token for a field: its named `env_parser` if declared, else its declared type.
///
/// Panics when a modeled key uses an unsupported parser.
fn decode_token(key: &str, info: Option<&FieldInfo>) -> &'static str {
    if let Some(parser) = info.and_then(|i| i.env_parser.as_ref()) {
        return match parser {
            EnvParser::Json => "EnvDecode::Json",
            EnvParser::CommaSeparated => "EnvDecode::CommaSeparated",
            EnvParser::CsvCommaSeparated => panic!(
                "modeled key `{key}` uses `csv_comma_separated`; implement a CSV parser before supporting this key"
            ),
            EnvParser::CommaAndSpaceSeparated => "EnvDecode::CommaAndSpaceSeparated",
            EnvParser::CommaThenSpaceSeparated => "EnvDecode::CommaThenSpaceSeparated",
            EnvParser::JsonListOrCommaSeparated => "EnvDecode::JsonListOrCommaSeparated",
            EnvParser::JsonListOrSpaceSeparated => "EnvDecode::JsonListOrSpaceSeparated",
            EnvParser::TracesSpan => "EnvDecode::TracesSpan",
        };
    }
    match info.map(|i| &i.value_type) {
        Some(FieldType::String) => "EnvDecode::RawString",
        Some(FieldType::Bool) => "EnvDecode::Bool",
        Some(FieldType::Integer) => "EnvDecode::Integer",
        Some(FieldType::Float) => "EnvDecode::Float",
        Some(FieldType::StringList) => "EnvDecode::StringList",
        Some(FieldType::Duration) => "EnvDecode::DurationString",
        // A map/object or an otherwise not-modeled type decodes as a JSON document, matching the
        // Agent's raw-JSON handling for map-typed keys (for example `additional_endpoints`).
        Some(FieldType::Unknown) | None => "EnvDecode::JsonValue",
    }
}
