//! Build-time codegen for the environment-variable overlay table.
//!
//! Emits [`EnvOverlayKey`] entries for `support: full` / `support: partial` keys: each maps the flat
//! top-level key an environment variable produces onto the nested path the typed deserializer reads.
//!
//! Each key emits one entry whose `flats` array lists every flat env form that reaches it, **in
//! schema priority order, highest priority first**. That order is load-bearing: at runtime
//! `apply_env_overlay` takes the value from the first present flat, so the order here determines
//! which env var wins when several are set (for example `dd_url` prefers `DD_DD_URL` over `DD_URL`).
//! It is preserved straight from the schema's declaration order, so do not reorder it.
//!
//! The flat forms are derived from the schema's [`EnvBinding`], mirroring the Agent's `bindEnv`:
//! - [`EnvBinding::Standard`] uses the derived `DD_` + `UPPER(dotted_path)` name, whose flat form is
//!   `a.b.c` -> `a_b_c`.
//! - [`EnvBinding::Overridden`] declares explicit env var names that replace that default; each flat
//!   form comes from a name (strip the `DD_` prefix, convert to lowercase), in declared order. The derived
//!   default is *not* included, matching the Agent, which does not accept it once explicit names are
//!   declared.
//! - [`EnvBinding::None`] (the `no-env` tag) also uses the mechanical flat form. The tag means the
//!   field is not bound through the Agent's standard per-key `BindEnv`, not that no env var exists:
//!   `proxy.http`, for instance, is tagged `no-env` yet is reachable through `DD_PROXY_HTTP` (a
//!   standard-form name) via the Agent's bespoke proxy handling, and the nested typed model still
//!   needs it relocated. A field that genuinely has no env var only yields a dead entry that never
//!   matches, so this is safe.
//!
//! A flat form is dropped when it already equals the single top-level key the deserializer reads (a
//! single-segment path whose name matches the flat form): the value is already in place. A key with
//! no remaining flats after that filter emits no entry at all.
//!
//! This table carries only paths, not value shapes. Relocation moves a flat value verbatim; a string
//! list set from a space-separated env var is split later by the string-list leaf's shape-tolerant
//! deserializer (see the crate's `list_de`), not here. So even a top-level string list already at
//! its destination needs no entry; the deserializer reshapes it in place.

use std::fmt::Write as _;
use std::path::Path;

use datadog_agent_config_overlay_model::schema_gen::{EnvBinding, FieldInfo};
use datadog_agent_config_overlay_model::{KnownEntry, SchemaOverlay};
use indexmap::IndexMap;

/// The environment-variable prefix removed before converting the name to lowercase. Hardcoded here
/// to avoid a build-dependency edge from this crate onto the commons crate.
const ENV_VAR_PREFIX: &str = "DD_";

/// Derives the flat top-level key produced for an environment variable: remove the `DD_` prefix and
/// lowercase the remainder. Single underscores remain part of the flat key rather than becoming
/// nesting separators.
fn flat_from_env_var(env_var: &str) -> String {
    env_var.strip_prefix(ENV_VAR_PREFIX).unwrap_or(env_var).to_lowercase()
}

pub fn generate(overlay: &SchemaOverlay, schema_map: &IndexMap<String, FieldInfo>, manifest_dir: &Path) {
    let mut keys: Vec<&String> = overlay
        .inventory
        .iter()
        .filter(|(_, entry)| matches!(entry, KnownEntry::Full(_) | KnownEntry::Partial(_)))
        .map(|(key, _)| key)
        .collect();
    keys.sort();

    let mut out = String::new();
    writeln!(
        out,
        "// @generated by build.rs from core_schema.yaml + schema_overlay.yaml — DO NOT EDIT"
    )
    .unwrap();
    writeln!(out, "// Regenerate by running `cargo build -p datadog-agent-config`.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use crate::env_overlay::EnvOverlayKey;").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "/// Supported Datadog keys whose flat env forms must be relocated into their nested slot."
    )
    .unwrap();
    writeln!(
        out,
        "/// Within each entry, `flats` is ordered highest priority first; see `apply_env_overlay`."
    )
    .unwrap();
    writeln!(out, "pub static ENV_OVERLAY_KEYS: &[EnvOverlayKey] = &[").unwrap();

    for key in keys {
        let info = schema_map.get(key.as_str());

        // The flat env form(s) that reach this key, in schema priority order (highest first).
        // Explicit overrides replace the derived default (mirroring the Agent's `bindEnv`);
        // everything else — standard, no-env, or a missing schema entry — falls back to the
        // mechanical `a.b.c` -> `a_b_c` form. See the module docs for why `no-env` still emits.
        let candidate_flats: Vec<String> = match info.map(|i| &i.env) {
            Some(EnvBinding::Overridden(env_vars)) => env_vars.iter().map(|e| flat_from_env_var(e)).collect(),
            None | Some(EnvBinding::Standard | EnvBinding::None) => vec![key.replace('.', "_")],
        };

        let segments: Vec<&str> = key.split('.').collect();

        // Drop any flat that already lands where the deserializer reads it (a single-segment path
        // whose key equals the flat): those need no relocation. Order is preserved.
        let flats: Vec<String> = candidate_flats
            .into_iter()
            .filter(|flat| !(segments.len() == 1 && segments[0] == flat))
            .collect();
        if flats.is_empty() {
            continue;
        }

        let flats_lit = flats
            .iter()
            .map(|flat| format!("\"{}\"", flat))
            .collect::<Vec<_>>()
            .join(", ");
        let path_lit = segments
            .iter()
            .map(|seg| format!("\"{}\"", seg))
            .collect::<Vec<_>>()
            .join(", ");

        writeln!(out, "    EnvOverlayKey {{").unwrap();
        writeln!(out, "        flats: &[{}],", flats_lit).unwrap();
        writeln!(out, "        path: &[{}],", path_lit).unwrap();
        writeln!(out, "    }},").unwrap();
    }

    writeln!(out, "];").unwrap();

    let path = manifest_dir.join("src/generated/env_overlay_keys.rs");
    let existing = std::fs::read_to_string(&path).unwrap_or_default();
    if existing != out {
        std::fs::write(&path, &out).unwrap_or_else(|e| panic!("cannot write {}: {}", path.display(), e));
    }

    // Canonicalize to the repo's rustfmt style. Best-effort: a missing rustfmt is not fatal.
    let _ = std::process::Command::new("rustfmt")
        .arg("--edition")
        .arg("2021")
        .arg(&path)
        .status();
}
